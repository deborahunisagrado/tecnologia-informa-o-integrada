def real_para_dolar(valor_em_real):
    taxa_de_cambio = 4.9  
    return valor_em_real / taxa_de_cambio

def dolar_para_real(valor_em_dolar):
    taxa_de_cambio = 4.9  
    return valor_em_dolar * taxa_de_cambio

def real_para_euro(valor_em_real):
    taxa_de_cambio = 5.43  
    return valor_em_real / taxa_de_cambio

def euro_para_real(valor_em_euro):
    taxa_de_cambio = 5.43  
    return valor_em_euro * taxa_de_cambio
